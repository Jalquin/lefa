            <p>Váš souhlas (resp. souhlasy) se zpracováním osobních údajů můžete kdykoli bezplatně zcela či
                částečně odvolat, a to prostřednictvím e-mailové zprávy zaslané na e-mailovou adresu uvedenou na
                webových stránkách správce či dopisem zaslaným na adresu správce uvedenou na webových stránkách
                správce. V důsledku odvolání souhlasu nebudou Vaše údaje v rozsahu takto odvolaného souhlasu
                dále zpracovávány, nebude-li jejich zpracování současně založeno i na jiném důvodu, které by nás
                k dalšímu zpracování opravňovalo (jiným důvodem zpracování může být např. nezbytnost zpracování
                ke splnění smlouvy či z důvodu našeho oprávněného zájmu).</p>
            <p>Odvoláním souhlasu není dotčena zákonnost zpracování založená na souhlasu uděleném před jeho
